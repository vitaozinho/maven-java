package model;

public class Escritor {

	private int nrCPF;
	private String nmEscritor;
	private String endEscritor;
	private int nrCnpjEditora;
	
	public int getNrCPF() {
		return nrCPF;
	}
	public void setNrCPF(int nrCPF) {
		this.nrCPF = nrCPF;
	}
	public String getNmEscritor() {
		return nmEscritor;
	}
	public void setNmEscritor(String nmEscritor) {
		this.nmEscritor = nmEscritor;
	}
	public String getEndEscritor() {
		return endEscritor;
	}
	public void setEndEscritor(String endEscritor) {
		this.endEscritor = endEscritor;
	}
	public int getNrCnpjEditora() {
		return nrCnpjEditora;
	}
	public void setNrCnpjEditora(int nrCnpjEditora) {
		this.nrCnpjEditora = nrCnpjEditora;
	}
	
	}
