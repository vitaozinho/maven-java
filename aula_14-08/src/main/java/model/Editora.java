package model;

public class Editora {
	
	private int nrCNPJ;
	private String nmEditora;
	private String endEditora;
	
	public int getNrCNPJ() {
		return nrCNPJ;
	}
	public void setNrCNPJ(int nrCNPJ) {
		this.nrCNPJ = nrCNPJ;
	}
	public String getNmEditora() {
		return nmEditora;
	}
	public void setNmEditora(String nmEditora) {
		this.nmEditora = nmEditora;
	}
	public String getEndEditora() {
		return endEditora;
	}
	public void setEndEditora(String endEditora) {
		this.endEditora = endEditora;
	}
	
}
