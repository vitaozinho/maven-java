package model;

public class Livro {
 private int cdLivro;
 private String nmLivro;
 private int nrPag;
 private int nrCnpjEditora;
 private int nrCpfEscritor;
public int getCdLivro() {
	return cdLivro;
}
public void setCdLivro(int cdLivro) {
	this.cdLivro = cdLivro;
}
public String getNmLivro() {
	return nmLivro;
}
public void setNmLivro(String nmLivro) {
	this.nmLivro = nmLivro;
}
public int getNrPag() {
	return nrPag;
}
public void setNrPag(int nrPag) {
	this.nrPag = nrPag;
}
public int getNrCnpjEditora() {
	return nrCnpjEditora;
}
public void setNrCnpjEditora(int nrCnpjEditora) {
	this.nrCnpjEditora = nrCnpjEditora;
}
public int getNrCpfEscritor() {
	return nrCpfEscritor;
}
public void setNrCpfEscritor(int nrCpfEscritor) {
	this.nrCpfEscritor = nrCpfEscritor;
}
 
}
